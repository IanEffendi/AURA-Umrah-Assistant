st.title("ጲB AURA: Asisten Umrah & Haji Anda")
tab1, tab2 = st.tabs(["ᄒ Chat dengan AURA", "፣ Form Pendaftaran"])

with tab1:
    if "messages" not in st.session_state: st.session_state.messages = []
    for m in st.session_state.messages:
        with st.chat_message(m["role"]): st.markdown(m["content"])

    if prompt := st.chat_input("Tanya AURA tentang paket Umrah..."):
        st.chat_message("user").markdown(prompt)
        st.session_state.messages.append({"role": "user", "content": prompt})
        response = model.start_chat(history=[]).send_message(prompt)
        with st.chat_message("assistant"): st.markdown(response.text)
        st.session_state.messages.append({"role": "assistant", "content": response.text})

with tab2:
    st.header("Formulir Minat Jamaah")
    with st.form("form_pendaftaran"):
        nama = st.text_input("Nama Lengkap")
        email = st.text_input("Email")
        no_hp = st.text_input("Nomor WhatsApp (628...)")
        alamat = st.text_area("Alamat")
        paket = st.selectbox("Pilihan Paket", ["Umrah Reguler", "Umrah Plus", "Haji Furoda"])
        submit = st.form_submit_button("Kirim Data & Chat Admin")

        if submit:
            if nama and no_hp:
                try:
                    sheet = connect_to_sheets()
                    if sheet:
                        sheet.append_row([nama, email, no_hp, alamat, paket, datetime.now().strftime("%Y-%m-%d")])
                        send_telegram_notif(nama, paket, no_hp)
                        admin_phone = "6281252539170"
                        msg = urllib.parse.quote(f"Assalamu'alaikum Admin. Saya {nama} tertarik paket {paket}.")
                        st.success("Data tersimpan! Klik tombol di bawah:")
                        st.markdown(f'[ Hubungi Admin via WhatsApp ](https://wa.me/{admin_phone}?text={msg})')
                except Exception as e: st.error(f"Error: {e}")
            else: st.warning("Lengkapi data!")