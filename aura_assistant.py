import streamlit as st
import google.generativeai as genai
import gspread
from oauth2client.service_account import ServiceAccountCredentials
from datetime import datetime
import urllib.parse
import requests

# ==========================================
# KONFIGURASI HALAMAN STREAMLIT
# ==========================================
st.set_page_config(page_title="AURA Umrah Assistant", page_icon="ጲB", layout="centered")

# ==========================================
# INISIALISASI GEMINI API
# ==========================================
try:
    genai.configure(api_key=st.secrets["GEMINI_API_KEY"])
except Exception as e:
    st.error("Gagal memuat API Key dari secrets.toml. Pastikan cell pertama sudah dijalankan dengan data benar.")

# Persona AURA
system_instruction = """
Anda adalah AURA (Asisten Umrah Ramah & Amanah), representasi virtual dari biro perjalanan Umrah & Haji terpercaya.
Gaya bahasa Anda: Sangat ramah, Islami (salam, masyaAllah, alhamdulillah), sopan, dan profesional.
Tujuan utama: Memberikan informasi paket dan mengarahkan jamaah untuk mengisi form pendaftaran di tab 'Form Pendaftaran'.
"""

model = genai.GenerativeModel('gemini-1.5-flash', system_instruction=system_instruction)

# ==========================================
# FUNGSI PEMBANTU
# ==========================================
def connect_to_sheets():
    scope = ["https://spreadsheets.google.com/feeds", "https://www.googleapis.com/auth/drive"]
    creds_dict = dict(st.secrets["gcp_service_account"])
    creds = ServiceAccountCredentials.from_json_keyfile_dict(creds_dict, scope)
    client = gspread.authorize(creds)

    # GANTI 'ID_SPREADSHEET_ANDA_DISINI' dengan ID spreadsheet Anda
    spreadsheet_id = "ID_SPREADSHEET_ANDA_DISINI"
    if spreadsheet_id == "ID_SPREADSHEET_ANDA_DISINI":
        st.error("Silakan ganti ID_SPREADSHEET_ANDA_DISINI dengan ID asli di kode Python ini.")
        return None

    sheet = client.open_by_key(spreadsheet_id).sheet1
    return sheet

def send_telegram_notif(nama, paket, no_hp):
    token = st.secrets.get("TELEGRAM_BOT_TOKEN", "YOUR_BOT_TOKEN")
    chat_id = st.secrets.get("TELEGRAM_CHAT_ID", "YOUR_CHAT_ID")
    pesan = f"☑• *Pendaftar Baru AURA*\n\nNama: {nama}\nPaket: {paket}\nWA: https://wa.me/{no_hp}"
    url = f"https://api.telegram.org/bot{token}/sendMessage?chat_id={chat_id}&text={pesan}&parse_mode=Markdown"
    requests.get(url)

