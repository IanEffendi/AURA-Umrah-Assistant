def generate_respon_aura(nama_jamaah, paket_pilihan):
    instruksi_sistem = "Nama Anda adalah AURA. Sapa hangat dan jelaskan paket."
    try:
        # Menggunakan identifier yang terkonfirmasi ada di list_models()
        model = genai.GenerativeModel(
            model_name='gemini-flash-latest',
            system_instruction=instruksi_sistem
        )
        response = model.generate_content(f"{nama_jamaah} tanya {paket_pilihan}")
        return response.text
    except Exception as e:
        return f"Terjadi kesalahan: {str(e)}"

