import gspread
from google.oauth2.service_account import Credentials

def simpan_ke_google_sheets(data_list):
    # Ganti 'credentials.json' dengan file Anda
    scopes = ["https://www.googleapis.com/auth/spreadsheets"]
    creds = Credentials.from_service_account_file("credentials.json", scopes=scopes)
    client = gspread.authorize(creds)

    # Buka spreadsheet berdasarkan Nama atau URL
    sheet = client.open("Data Jamaah AURA").sheet1
    sheet.append_row(data_list)
    print("Data berhasil dikirim ke Google Sheets!")

